package class23;

class Stars4 {
public static void main(String[] args) {
		for(int i=1; i<=3;i++) {
			for(int j=1;j<=4;j++) {
				System.out.print(i);
			}
			System.out.println();
		}
		System.out.println("-------------------------");
		for(int i=1; i<=3;i++) {
			for(int j=1;j<=3;j++) {
				if(i==j) {
				System.out.print(i);
			}
			System.out.println();
		}
	}
}
}